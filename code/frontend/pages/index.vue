import axios from 'axios'
export default {
    data() {
        return {
            feedback: {
                type: '',
                budget: '',
                client_name: '',
                client_contact: '',
                comment: '',
            },
            types: [
                {
                    id: 0,
                    name: 'Лендинг'
                },
                {
                    id: 1,
                    name: 'Интернет-магазин'
                },
                {
                    id: 2,
                    name: 'Мобильное приложение'
                },
                {
                    id: 3,
                    name: 'Продуктовая разработка'
                },
                {
                    id: 4,
                    name: 'Сайт-визитка'
                },
                {
                    id: 5,
                    name: 'Другое'
                },
            ],
            show_success: false,
        }
    },
    computed: {
        formattedBudget() {
            if (!this.feedback.budget || parseFloat(this.feedback.budget) <= 0) return '';

            return parseFloat(this.feedback.budget).toLocaleString('ru-RU') + ' ₸';
        }
    },
    methods: {

        async send_feedback() {
            if (this.feedback.client_name.length <= 0) {
                this.$refs.name.classList.add('error')

                return
            }
            else if (this.feedback.client_contact.length <= 0) {
                this.$refs.com_method.classList.add('error')

                return
            }
            this.$refs.name.classList.remove('error')
            this.$refs.com_method.classList.remove('error')


            const path = 'https://neutron-agency.com/api/main/new-application/'
            axios
                .post(path, this.feedback)
                .then(res => {
                    console.log(res)


                    this.open_success()

                    this.feedback = {
                        type: '',
                        budget: '',
                        client_name: '',
                        client_contact: '',
                        comment: '',
                    }
                })
                .catch(error => {
                    console.log(error)
                })

        },
        handleInput(event) {
            let inputValue = event.target.value;

            inputValue = inputValue.replace(/\D/g, '');

            if (parseFloat(inputValue) <= 0) {
                this.feedback.budget = null;
            } else {

                this.feedback.budget = inputValue;
            }
        },
        open_success() {
            this.show_success = !this.show_success,
                document.body.style.overflow = 'hidden';
        },
        close_success() {
            this.show_success = !this.show_success,
                document.body.style.overflow = '';
        },
    }
}
